export function textFieldTemplate(): ComponentFiles {
  const component = `${HEADER}import * as React from 'react';
import '../styles.css';

export interface TextFieldProps
  extends Omit<React.InputHTMLAttributes<HTMLInputElement>, 'id' | 'aria-describedby'> {
  /** Visible label — always rendered and programmatically associated. */
  label: string;
  /** Optional helper text, linked via aria-describedby. */
  description?: string;
  /** Validation message. Sets aria-invalid and is announced politely. */
  error?: string;
}

export function TextField({ label, description, error, required, ...rest }: TextFieldProps) {
  const id = React.useId();
  const descriptionId = id + '-description';
  const errorId = id + '-error';
  const describedBy =
    [description ? descriptionId : null, error ? errorId : null].filter(Boolean).join(' ') ||
    undefined;

  return (
    <div className="ds-field">
      <label className="ds-field__label" htmlFor={id}>
        {label}
        {required ? <span aria-hidden="true"> *</span> : null}
      </label>
      {description ? (
        <p className="ds-field__description" id={descriptionId}>
          {description}
        </p>
      ) : null}
      <input
        id={id}
        className="ds-input"
        aria-describedby={describedBy}
        aria-invalid={error ? true : undefined}
        required={required}
        {...rest}
      />
      <p className="ds-field__error" id={errorId} aria-live="polite">
        {error ?? ''}
      </p>
    </div>
  );
}
`;

  const story = `${HEADER}import type { Meta, StoryObj } from '@storybook/react';
import { TextField } from './TextField';

const meta = {
  title: 'Components/TextField',
  component: TextField,
  tags: ['autodocs'],
  args: { label: 'Email address' },
} satisfies Meta<typeof TextField>;

export default meta;
type Story = StoryObj<typeof meta>;

export const Default: Story = {};
export const WithDescription: Story = {
  args: { description: "We'll only use this to send order updates." },
};
export const Required: Story = { args: { required: true } };
export const WithError: Story = {
  args: { error: 'Enter a valid email address.', defaultValue: 'not-an-email' },
};
`;

  const test = axeTest(
    'TextField',
    `  it('has no axe violations in default, described, and error states', async () => {
    const { container } = render(
      <main>
        <TextField label="Email address" />
        <TextField label="Password" description="Must be at least 12 characters." required />
        <TextField label="Username" error="That username is taken." defaultValue="ben" />
      </main>,
    );
    await expectNoAxeViolations(container);
  });
`,
  );

  return { name: 'TextField', component, story, test };
}

