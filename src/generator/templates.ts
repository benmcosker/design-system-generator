export function alertTemplate(): ComponentFiles {
  const component = `${HEADER}import * as React from 'react';
import '../styles.css';

export interface AlertProps extends React.HTMLAttributes<HTMLDivElement> {
  tone?: 'info' | 'success' | 'warning' | 'danger';
  title?: string;
}

/**
 * Danger alerts use role="alert" (assertive) because they demand attention;
 * everything else uses role="status" (polite) to avoid interrupting
 * screen-reader users mid-task.
 */
export function Alert({ tone = 'info', title, className, children, ...rest }: AlertProps) {
  const classes = ['ds-alert', 'ds-alert--' + tone, className].filter(Boolean).join(' ');
  const role = tone === 'danger' ? 'alert' : 'status';
  return (
    <div className={classes} role={role} {...rest}>
      <div>
        {title ? <p className="ds-alert__title">{title}</p> : null}
        {children}
      </div>
    </div>
  );
}
`;

  const story = `${HEADER}import type { Meta, StoryObj } from '@storybook/react';
import { Alert } from './Alert';

const meta = {
  title: 'Components/Alert',
  component: Alert,
  tags: ['autodocs'],
  args: { title: 'Heads up', children: 'Your trial ends in 3 days.' },
} satisfies Meta<typeof Alert>;

export default meta;
type Story = StoryObj<typeof meta>;

export const Info: Story = {};
export const Success: Story = {
  args: { tone: 'success', title: 'Saved', children: 'Your changes are live.' },
};
export const Warning: Story = {
  args: { tone: 'warning', title: 'Storage almost full', children: 'You have 2% remaining.' },
};
export const Danger: Story = {
  args: { tone: 'danger', title: 'Payment failed', children: 'Update your card to continue.' },
};
`;

  const test = axeTest(
    'Alert',
    `  it('has no axe violations across tones', async () => {
    const { container } = render(
      <main>
        <Alert title="Heads up">Your trial ends in 3 days.</Alert>
        <Alert tone="success" title="Saved">Your changes are live.</Alert>
        <Alert tone="warning" title="Storage almost full">You have 2% remaining.</Alert>
        <Alert tone="danger" title="Payment failed">Update your card to continue.</Alert>
      </main>,
    );
    await expectNoAxeViolations(container);
  });
`,
  );

  return { name: 'Alert', component, story, test };
}

